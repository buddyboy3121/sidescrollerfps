package org.tools;

public class Vector2D {

	private float x, y;
	
	public Vector2D(float x, float y) {
		this.x = x;
		this.y = y;
	}
	
	/**
	 * Returns the X coordinate.
	 * @return the X coordinate.
	 */
	public float getX() {
		return this.x;
	}

	/**
	 * Returns the Y coordinate.
	 * @return the Y coordinate.
	 */
	public float getY() {
		return this.y;
	}
	
}
